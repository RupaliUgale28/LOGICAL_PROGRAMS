package ALL_PROGRAMS;

public class Pyramid 
{
	public static void main(String[] args) 
	{
			for(int a=4;a>=1;a--)
			{		
				for(int b=1;b<=a;b++)
				{
					System.out.print(" ");
					
				}
				for(int c=4;c>=a;c--)
				{
					System.out.print(" *");
					
				}
			System.out.println("");
			}
	}
	

}
